
nombre = input('Escribe tu nombre: ')
nombre = nombre.upper()
print('Tu nombre con la funcion nombre.upper():', nombre)

nombre = nombre.capitalize()
print('Tu nombre con la funcion nombre.capitalize():', nombre)

nombre = nombre.strip()
print('Tu nombre con la funcion nombre.strip():', nombre)

nombre = nombre.lower()
print('Tu nombre con la funcion nombre.lower():', nombre)

nombre = nombre.replace('a','u')
print("Tu nombre con la funcion nombre.replace('a','u'):", nombre)

tamano = len(nombre)
print('Utilizando la funcion len, tama√±o de las cadenas!', tamano)


